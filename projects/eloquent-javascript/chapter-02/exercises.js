// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(n) {
  let boardSize =n ;
  let board = "";
  for (let i = 0; i < boardSize; i++) {
    for (let j = 0; j < boardSize; j++) {
      if ((i + j) % 2 === 0) {
        board += " ";
      } else {
        board +=  "#";
      }
    }
    board += "\n";
  }
  console.log(board);
}
