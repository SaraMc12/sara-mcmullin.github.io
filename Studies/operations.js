/**
 * Arithmetic Operators:

Perform basic arithmetic operations like addition, subtraction, multiplication, division, and modulus.
Example: +, -, *, /, %

Comparison Operators:

Compare two values and return a Boolean result (true or false).
Example: ==, ===, !=, !==, <, >, <=, >=

Logical Operators:

Combine multiple conditions and return a Boolean result.
Example: && (logical AND), || (logical OR), ! (logical NOT)

Urnary Operator

increase or decrease the value of a variable.
Example: ++, --

 */

